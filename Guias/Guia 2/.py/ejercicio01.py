# Desarrollar algoritmo que permita mostrar en pantalla de manera automática (sin digitar) 
# una secuencia de números que se incrementan de 1 en 1. Debe mostrar del 1 al 10.

print("Secuencia de números del 1 al 10 (1 en 1): ")

for i in range(1, 11):
    if i>=1:
        print(f"{i}")